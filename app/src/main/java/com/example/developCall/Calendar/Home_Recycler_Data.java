package com.example.developCall.Calendar;

public class Home_Recycler_Data {
    private String recycle_Name;
    private String recycle_Target;
    private String recycle_Time;

    public String getRecycleName() {
        return recycle_Name;
    }

    public void setRecycleName(String recycle_Name) {
        this.recycle_Name = recycle_Name;
    }

    public String getRecycleTarget() {
        return recycle_Target;
    }

    public void setRecycleTarget(String recycle_Target) {
        this.recycle_Target = recycle_Target;
    }

    public String getRecycleTime() {
        return recycle_Time;
    }

    public void setRecycleTime(String recycle_Time) {
        this.recycle_Time = recycle_Time;
    }
}
